/**
 * The class MulByEight prints multiplication table by 8
 *
 * @author Yuri Grigorov
 */

public class MulByEight {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println("8 x " + i + " = " + 8 * i);
        }
    }
}
