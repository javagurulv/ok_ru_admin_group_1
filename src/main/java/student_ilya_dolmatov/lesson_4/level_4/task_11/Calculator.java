package student_ilya_dolmatov.lesson_4.level_4.task_11;

class Calculator {
    int firstNumber = 5;
    int secondNumber = 2;

    Calculator(){}

    int sum() {
        return this.firstNumber + this.secondNumber;
    }
    int sub() {
        return this.firstNumber - this.secondNumber;
    }
    int mul() {
        return this.firstNumber * this.secondNumber;
    }
    int div() {
        return this.firstNumber / this.secondNumber;
    }
}
